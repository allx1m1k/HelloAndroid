package ua.org.ua2012.weather;

public class Geonames {
	private String countrycode;
	private String fcl;
	private String fclName;
	private String fcode;
	private String fcodeName;
	private Number geonameId;
	private Number lat;
	private Number lng;
	private String name;
	private Number population;
	private String toponymName;
	private String wikipedia;

	public String getCountrycode() {
		return this.countrycode;
	}

	public void setCountrycode(String countrycode) {
		this.countrycode = countrycode;
	}

	public String getFcl() {
		return this.fcl;
	}

	public void setFcl(String fcl) {
		this.fcl = fcl;
	}

	public String getFclName() {
		return this.fclName;
	}

	public void setFclName(String fclName) {
		this.fclName = fclName;
	}

	public String getFcode() {
		return this.fcode;
	}

	public void setFcode(String fcode) {
		this.fcode = fcode;
	}

	public String getFcodeName() {
		return this.fcodeName;
	}

	public void setFcodeName(String fcodeName) {
		this.fcodeName = fcodeName;
	}

	public Number getGeonameId() {
		return this.geonameId;
	}

	public void setGeonameId(Number geonameId) {
		this.geonameId = geonameId;
	}

	public Number getLat() {
		return this.lat;
	}

	public void setLat(Number lat) {
		this.lat = lat;
	}

	public Number getLng() {
		return this.lng;
	}

	public void setLng(Number lng) {
		this.lng = lng;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Number getPopulation() {
		return this.population;
	}

	public void setPopulation(Number population) {
		this.population = population;
	}

	public String getToponymName() {
		return this.toponymName;
	}

	public void setToponymName(String toponymName) {
		this.toponymName = toponymName;
	}

	public String getWikipedia() {
		return this.wikipedia;
	}

	public void setWikipedia(String wikipedia) {
		this.wikipedia = wikipedia;
	}
}
